import android.util.Log;
public class Logs {

    public void LogE(String TAG,String message){
        Log.e(TAG,message);
    }

    public void LogI(String TAG,String message){
        Log.i(TAG,message);
    }

    public void LogV(String TAG,String message){
        Log.v(TAG,message);
    }

}
